// Class 1
const classLink1 = document.getElementById('class-link1');
const classInfo1 = document.getElementById('class-info1');

if (window.innerWidth >= 768) {
  // Desktop screens: Show class info on hover
  classLink1.addEventListener('mouseover', () => {
    classInfo1.innerHTML = `<ul>
      <li>Explore your creativity through various art mediums and learn the techniques of drawing and painting.</li>
      <li>Engage in hands-on projects, unleash your imagination, and express yourself artistically in a supportive environment.</li>
    </ul>`;
  });

  classLink1.addEventListener('mouseout', () => {
    classInfo1.innerHTML = '';
  });
} else {
  // Mobile and tablet devices: Show class info permanently
  classInfo1.innerHTML = `<ul>
    <li>Explore your creativity through various art mediums and learn the techniques of drawing and painting.</li>
    <li>Engage in hands-on projects, unleash your imagination, and express yourself artistically in a supportive environment.</li>
  </ul>`;
}

